defmodule Exical.Todo do
  @moduledoc """
  [VTODO](https://www.rfc-editor.org/rfc/rfc5545#section-3.6.2) component.
  """
  defstruct alarms: [],
            dtstamp: nil,
            dtstart: nil,
            uid: nil,
            class: nil,
            completed: nil,
            created: nil,
            description: nil,
            geo: nil,
            last_mod: nil,
            location: nil,
            organizer: nil,
            percent: nil,
            priority: nil,
            recurid: nil,
            seq: nil,
            status: nil,
            summary: nil,
            url: nil,
            rrule: nil,
            due: nil,
            duration: nil,
            attendee: nil,
            attach: nil,
            categories: nil,
            comments: nil,
            exdates: [],
            contacts: nil,
            rstatuses: nil,
            rdate: nil,
            resources: [],
            related: nil

  def parse(%Exical.Todo{
        alarms: alarms,
        dtstamp: dtstamp,
        dtstart: dtstart,
        uid: uid,
        class: class,
        completed: completed,
        created: created,
        description: description,
        geo: geo,
        last_mod: last_mod,
        location: location,
        organizer: organizer,
        percent: percent,
        priority: priority,
        recurid: recurid,
        seq: seq,
        status: status,
        summary: summary,
        url: url,
        rrule: rrule,
        due: due,
        duration: duration,
        attendee: attendee,
        attach: attach,
        categories: categories,
        comments: comments,
        exdates: exdates,
        contacts: contacts,
        rstatuses: rstatuses,
        rdate: rdate,
        resources: resources,
        related: related
      }) do
    %Exical.Todo{
      alarms: alarms,
      dtstamp: dtstamp,
      dtstart: dtstart,
      uid: uid,
      class: class,
      completed: completed,
      created: created,
      description: description,
      geo: geo,
      last_mod: last_mod,
      location: location,
      organizer: organizer,
      percent: percent,
      priority: priority,
      recurid: recurid,
      seq: seq,
      status: status,
      summary: summary,
      url: url,
      rrule: rrule,
      due: due,
      duration: duration,
      attendee: attendee,
      attach: attach,
      categories: categories,
      comments: comments,
      exdates: exdates,
      contacts: contacts,
      rstatuses: rstatuses,
      rdate: rdate,
      resources: resources,
      related: related
    }
  end
end
